        private static string BuildString(ReadOnlySpan<byte> bytes, Dictionary<byte, ushort> charTable)
        {
            char[] chars = new char[bytes.Length];
            var stringLen = 0;

            for (int i = 0; i < chars.Length; i++)
            {
                if (bytes[i] >= 0x80 && bytes[i] <= 0x89) continue;
                if (bytes[i] >= 0x8B && bytes[i] <= 0x9F) continue;

                if (charTable.TryGetValue(bytes[i], out ushort value))
                {
                    chars[i] = Convert.ToChar(value);
                    stringLen++;
                }
                else
                {
                    Logger.Send(LogStatus.Warning, $"Can't find value in coding table on position 0x{i:X}");
                }
            }

            return new string(chars[..stringLen]);
        }
        internal static string BytesToString(ReadOnlySpan<byte> bytes)
        {
            if (bytes.Length == 0) return "";

            switch (bytes[0])
            {
                case 0x01: return BuildString(bytes[1..], iso8859_5);
                case 0x02: return BuildString(bytes[1..], iso8859_6);
                case 0x03: return BuildString(bytes[1..], iso8859_7);
                case 0x04: return BuildString(bytes[1..], iso8859_8);
                case 0x05: return BuildString(bytes[1..], iso8859_9);
                case 0x06: return BuildString(bytes[1..], iso8859_10);
                case 0x07: return BuildString(bytes[1..], iso8859_11);
                case 0x09: return BuildString(bytes[1..], iso8859_13);
                case 0x0A: return BuildString(bytes[1..], iso8859_14);
                case 0x0B: return BuildString(bytes[1..], iso8859_15);
                case 0x10:
                    {
                        var exCode = BinaryPrimitives.ReadUInt16BigEndian(bytes[1..]);
                        switch (exCode)
                        {
                            case 0x0001: return BuildString(bytes[3..], iso8859_1);
                            case 0x0002: return BuildString(bytes[3..], iso8859_2);
                            case 0x0003: return BuildString(bytes[3..], iso8859_3);
                            case 0x0004: return BuildString(bytes[3..], iso8859_4);
                            case 0x0005: return BuildString(bytes[3..], iso8859_5);
                            case 0x0006: return BuildString(bytes[3..], iso8859_6);
                            case 0x0007: return BuildString(bytes[3..], iso8859_7);
                            case 0x0008: return BuildString(bytes[3..], iso8859_8);
                            case 0x0009: return BuildString(bytes[3..], iso8859_9);
                            case 0x000A: return BuildString(bytes[3..], iso8859_10);
                            case 0x000B: return BuildString(bytes[3..], iso8859_11);
                            case 0x000D: return BuildString(bytes[3..], iso8859_13);
                            case 0x000E: return BuildString(bytes[3..], iso8859_14);
                            case 0x000F: return BuildString(bytes[3..], iso8859_15);
                            default:
                                {
                                    throw new NotImplementedException($"Not impement extended coding type 0x{exCode:X}");
                                }
                        }
                    }
                case 0x11: throw new NotImplementedException("Not impement ISO-10646 coding table");
                case 0x12: throw new NotImplementedException("Not impement KSX1001-2004 coding table");
                case 0x13: throw new NotImplementedException("Not impement GB-2312-1980 coding table");
                case 0x14: throw new NotImplementedException("Not impement Big5 coding table");
                case 0x15:
                    {
                        return BuildString(bytes[1..], UTF8_table);
                    }
                case byte n when n >= 0x20 && n <= 0xFF:
                    {
                        return BuildString(bytes, iso8859_1);
                    }
                default:
                    {
                        return BuildString(bytes, iso8859_1);
                    }

            }
        }
